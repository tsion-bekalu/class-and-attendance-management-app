class StudentAttendance {
  final String name;
  final String email;
  final String overallPercentage;
  final String presentRatio;
  final String absentRatio;

  StudentAttendance({
    required this.name,
    required this.email,
    required this.overallPercentage,
    required this.presentRatio,
    required this.absentRatio,
  });
}